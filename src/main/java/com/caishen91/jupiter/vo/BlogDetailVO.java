package com.caishen91.jupiter.vo;

import com.caishen91.jupiter.model.BaseEntity;

public class BlogDetailVO extends BaseEntity{

	private static final long serialVersionUID = 302292696810649151L;
	
	// 公众号名称
	private String blogName; 
	// 公众号账号类型
	private int type;
	// 公众号头像
	private String headImg;
	// 公众号描述
	private String desc;
	// 公众号认证情况
	private int authStatus;
	// 企业名称
	private String enterpriseName;
	// 企业类型
	private int enterpriseType;
	// 公司邮箱
	private String email;
	// 社会信用代码
	private String scc;
	// 公众号管理员姓名
	private String blogManagerName;
	// 昵称
	private String nickName;
	// 公众号管理员手机号
	private String mobile;
	
	public String getBlogName() {
		return blogName;
	}
	public void setBlogName(String blogName) {
		this.blogName = blogName;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getHeadImg() {
		return headImg;
	}
	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public int getAuthStatus() {
		return authStatus;
	}
	public void setAuthStatus(int authStatus) {
		this.authStatus = authStatus;
	}
	public String getEnterpriseName() {
		return enterpriseName;
	}
	public void setEnterpriseName(String enterpriseName) {
		this.enterpriseName = enterpriseName;
	}
	public int getEnterpriseType() {
		return enterpriseType;
	}
	public void setEnterpriseType(int enterpriseType) {
		this.enterpriseType = enterpriseType;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getScc() {
		return scc;
	}
	public void setScc(String scc) {
		this.scc = scc;
	}
	public String getBlogManagerName() {
		return blogManagerName;
	}
	public void setBlogManagerName(String blogManagerName) {
		this.blogManagerName = blogManagerName;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
}

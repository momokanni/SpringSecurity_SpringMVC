package com.caishen91.jupiter.enums;

public enum PermitRoleTag {

	firstAudit("firstAudit", "初审"),
	secondAudit("secondAudit", "二审"),
	finalAudit("finalAudit", "终审"),
	businessCheck("businessCheck", "业务复核"),
	businessAudit("businessAudit", "业务审批"),
	loanAudit("loanAudit", "放款审批"),
	allocate("allocate", "业务分配"),
	turnAudit("turnAudit", "转单审核"),
	reloanCheck("reloanCheck", "展期复核");
	
	private String tag;
	
	private String desc;

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	private PermitRoleTag(String tag, String desc) {
		this.tag = tag;
		this.desc = desc;
	}
	
}
